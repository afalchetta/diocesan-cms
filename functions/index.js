// index.js

const { onDocumentCreated } = require("firebase-functions/v2/firestore");
const { onSchedule } = require("firebase-functions/v2/scheduler");
const { initializeApp } = require("firebase-admin/app");
const { getFirestore, FieldValue, Timestamp } = require("firebase-admin/firestore");
const nodemailer = require("nodemailer");
const Imap = require("imap");
require("dotenv").config();
const { simpleParser } = require("mailparser");

// ✅ Initialize the app and get Firestore
initializeApp();
const db = getFirestore();

const imapUser = process.env.IMAP_USER;
const imapPass = process.env.IMAP_PASS;

// ✅ Read directly from .env
const FROM_EMAIL = process.env.OUTLOOK_EMAIL;
const FROM_PASS = process.env.OUTLOOK_PASSWORD;

if (!FROM_EMAIL || !FROM_PASS) {
  throw new Error("Missing OUTLOOK_EMAIL or OUTLOOK_PASSWORD in .env");
}

// ✅ Office365 SMTP transporter
const transporter = nodemailer.createTransport({
  host: "smtp.office365.com",
  port: 587,
  secure: false,
  auth: { user: FROM_EMAIL, pass: FROM_PASS },
});

exports.sendEmailOnNewCorr = onDocumentCreated("corrs/{corrId}", async (event) => {
  const snap = event.data;
  const data = snap.data();
  const id = event.params.corrId;

  if (!data) {
    console.log("No data found in new document");
    return;
  }

  const recipients = Array.isArray(data.assignedEmails) ? data.assignedEmails : [];

  if (recipients.length === 0) {
    console.log("No assignedEmails → skipping email.");
    return;
  }

  const mailOptions = {
    from: FROM_EMAIL,
    to: recipients,
    subject: `New Correspondence: ${data.subject || "(No Subject)"} | ID: ${id}`,
    html: `
      <h4>New Correspondence Created</h4>
      <h3>Subject:</strong> ${data.subject || "N/A"}</h3>
      <p>Type of Correspondence: <strong>${data.type}</strong></p>
      <p>Priority <strong>${data.priority}</strong></p>
      <p><strong>Details:</strong></p>
      <p>${data.details || "N/A"}</p>
      <hr>

  <p>
    <a href="https://cms.camdendiocese.org">
      View This Correspondence
    </a>
  </p>

  <p>
    <a href="mailto:crm@employee.camdendiocese.org?subject=ID:${id}">
      Respond to this Correspondence Now
    </a>
  </p>

  <hr>

  <p>
    This email was automatically generated by the 
    <a href="https://cms.camdendiocese.org">Diocesan Correspondence System</a>.
  </p>
    `
  };

  try {
    await transporter.sendMail(mailOptions);
    console.log("✅ Email sent to:", recipients);
  } catch (err) {
    console.error("❌ Failed to send email:", err);
  }
});


exports.sendEmailOnNewReply = onDocumentCreated("replies/{replyId}", async (event) => {
  const snap = event.data;
  const data = snap.data();

  if (!data) return;

  console.log("🔔 New reply detected:", data);

  const corrId = data.corrId;
  if (!corrId) {
    console.log("❌ Reply missing corrId — skipping email.");
    return;
  }

  // Get parent correspondence
  const corrDoc = await db.collection("corrs").doc(corrId).get();
  const corr = corrDoc.exists ? corrDoc.data() : {};
  if (!corr) {
    console.log("❌ Correspondence not found for ID:", corrId);
    return;
  }

  // Normalize assignedEmails
  let assignedEmails = [];

if (Array.isArray(data.assignedEmails)) {
  assignedEmails = data.assignedEmails;
} else if (typeof data.assignedEmails === "string") {
  assignedEmails = data.assignedEmails.split(",").map(e => e.trim());
} else if (Array.isArray(corr.assignedEmails)) {
  assignedEmails = corr.assignedEmails;
}

  // Always include agentEmail if available
  const agentEmail = data.agentEmail || corr.agentEmail || "";

  // Ensure createdByEmail exists
  const createdByEmail = data.createdByEmail || "";

  // Combine recipients: assignedEmails + agentEmail, exclude duplicates
 const recipients = [...new Set([
  ...assignedEmails,
  ...(agentEmail ? [agentEmail] : [])
])]
.filter(email => email !== createdByEmail);

  if (recipients.length === 0) {
    console.log("⚠ No recipients to notify — skipping email.");
    console.log("Assigned Emails:", assignedEmails);
    console.log("Agent Email:", agentEmail);
    console.log("Created By:", createdByEmail);
    return;
  }

  // Email subject and body
  const subject = `New Reply Added: ${data.subject || corr.subject || "Correspondence Update"}`;
  const body = `
    <h2>New Reply Added</h2>
    <h3>${data.subject || corr.subject || "N/A"}</h3>
    <p><strong>Reply:</strong></p>
    <p>${data.reply || "(No reply text)"}</p>
    <p><strong>Created By:</strong> ${data.createdBy || createdByEmail || "N/A"}</p>
    <hr>
    <p><a href="https://cms.camdendiocese.org/corr/${corrId}">View Correspondence</a></p>
    <hr>
    <p>This email was automatically generated by the Diocesan Correspondence System.</p>
  `;

  try {
  await transporter.sendMail({
    from: FROM_EMAIL,
    to: recipients,
    cc: "webguy@camdendiocese.org",
    subject,
    html: body,
  });

  console.log("✅ Email sent to:", recipients);
} catch (err) {
  console.error("❌ Failed to send email:", err);
}
});



