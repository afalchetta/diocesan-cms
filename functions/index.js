const imapConfig = {
  user: imapUser,
  password: imapPass,
  host: "cloudhost-76525.us-midwest-1.nxcli.net",
  port: 993,
  tls: true,
};

async function fetchEmails() {
  return new Promise((resolve, reject) => {
    const imap = new Imap(imapConfig);

    function openInbox(cb) {
      imap.openBox("INBOX", false, cb);
    }

    imap.once("ready", () => {
      openInbox(async (err) => {
        if (err) return reject(err);

        imap.search(["UNSEEN"], (err, results) => {
          if (err) return reject(err);
          if (!results || results.length === 0) {
            console.log("No new emails found.");
            imap.end();
            return resolve();
          }

          const f = imap.fetch(results, { bodies: "", markSeen: true });

          f.on("message", (msg) => {
            msg.on("body", async (stream) => {
              try {
                const parsed = await simpleParser(stream);
                const subject = parsed.subject?.trim() || "";
                const fromEmail =
                  parsed.from?.value?.[0]?.address || "unknown@example.com";
                const receivedAt = parsed.date
                  ? new Date(parsed.date)
                  : new Date();

                // Check if the subject matches "ID:someCorrId"
                const idMatch = subject.match(/^ID:(\S+)$/i);

                if (idMatch) {
  const corrId = idMatch[1];

  // ✅ Get the parent corr
  const corrDoc = await db.collection("corrs").doc(corrId).get();
  if (!corrDoc.exists) {
    console.log("No corr found for ID:", corrId);
    return;
  }
  const corr = corrDoc.data();

  // ✅ Create reply with assignedEmails and agentEmail from parent corr
  const replyData = {
    reply: parsed.text || "no content in this reply",
    createdBy: "", // optional: could map email to a user
    createdAt: receivedAt,
    corrId: corrId,
    createdByEmail: fromEmail,
    assignedEmails: corr.assignedEmails || [],
    agentEmail: corr.agentEmail || "",
  };

  // ✅ Optional: add the email sender to assignedEmails for future notifications
  replyData.assignedEmails = [...new Set([...replyData.assignedEmails, fromEmail])];

  await db.collection("replies").add(replyData);
  console.log("✅ Created reply for corr:", corrId);
} else {
                  // --- Create a new corr ---
    const newCorr = {
  subject: subject || "(No Subject)",
  priority: "High",
  details: parsed.text || "(No Body)",
  assignedEmails: [],
  agentEmail: fromEmail,
  dateReceived: receivedAt.toISOString(),
  addressedTo: "",
  agentName: "",
  senderContact: "",
  dueDate: '',
  createdAt: FieldValue.serverTimestamp(),
  type: "",
};

const docRef = await db.collection("corrs").add(newCorr);
await db.collection("corrs").doc(docRef.id).update({ id: docRef.id });

                  console.log("📩 Created new corr:", subject);
                }
              } catch (err) {
                console.error("Error parsing email:", err);
              }
            });
          });

          f.once("error", (err) => console.error("Fetch error:", err));
          f.once("end", () => {
            imap.end();
            resolve();
          });
        });
      });
    });

    imap.once("error", (err) => {
      console.error("IMAP connection error:", err);
      reject(err);
    });

    imap.connect();
  });
}

// 🔁 Run every 5 minutes
exports.pollEmails = onSchedule("every 5 minutes", async (event) => {
  console.log("Checking inbox for new emails...");
  try {
    await fetchEmails();
  } catch (error) {
    console.error("Error fetching emails:", error);
  }
});


